package prob01;

public class Printer {
	Printer() {

	}

	void println(int i) {
		System.out.println(i);
	}

	void println(boolean b) {
		System.out.println(b);
	}

	void println(String s) {
		System.out.println(s);
	}

	void println(double d) {
		System.out.println(d);
	}
}
